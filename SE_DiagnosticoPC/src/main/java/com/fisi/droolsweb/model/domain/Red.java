package com.fisi.droolsweb.model.domain;

public class Red {

	private int linkea;
	private int reconoce;
	private int habilitada;

	public int getLinkea() {
		return linkea;
	}

	public void setLinkea(int linkea) {
		this.linkea = linkea;
	}

	public int getReconoce() {
		return reconoce;
	}

	public void setReconoce(int reconoce) {
		this.reconoce = reconoce;
	}

	public int getHabilitada() {
		return habilitada;
	}

	public void setHabilitada(int habilitada) {
		this.habilitada = habilitada;
	}

}
