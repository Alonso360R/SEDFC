package com.fisi.droolsweb.model.domain;

public class Monitor {

	private int tipoCalidadImagen;
	private int conectado;
	private int funcionaCorriente;
	private int enciende;
	private int hayImagen;
	private int vgaConectado;
	private int vgaFunciona;

	public Monitor() {
	}

	public int getTipoCalidadImagen() {
		return tipoCalidadImagen;
	}

	public void setTipoCalidadImagen(int tipoCalidadImagen) {
		this.tipoCalidadImagen = tipoCalidadImagen;
	}

	public int getConectado() {
		return conectado;
	}

	public void setConectado(int conectado) {
		this.conectado = conectado;
	}

	public int getFuncionaCorriente() {
		return funcionaCorriente;
	}

	public void setFuncionaCorriente(int funcionaCorriente) {
		this.funcionaCorriente = funcionaCorriente;
	}

	public int getEnciende() {
		return enciende;
	}

	public void setEnciende(int enciende) {
		this.enciende = enciende;
	}

	public int getHayImagen() {
		return hayImagen;
	}

	public void setHayImagen(int hayImagen) {
		this.hayImagen = hayImagen;
	}

	public int getVgaConectado() {
		return vgaConectado;
	}

	public void setVgaConectado(int vgaConectado) {
		this.vgaConectado = vgaConectado;
	}

	public int getVgaFunciona() {
		return vgaFunciona;
	}

	public void setVgaFunciona(int vgaFunciona) {
		this.vgaFunciona = vgaFunciona;
	}

	

}
